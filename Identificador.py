# Programa que identifica quem é a pessoa

Nome = ('Marquinhos')

if Nome == 'Vitória Rosa':
    print('Ela é o amor da sua vida')

elif Nome == 'Maria Libencia':
    print('Ela é sua mãe')

elif Nome == 'Mayla':
    print('Ela é sua filhota')

